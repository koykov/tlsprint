func TestParser(t *testing.T) {
	for i, stg := range stages {
		t.Run(strconv.Itoa(i), func(t *testing.T) {
			vec := New()
			err := vec.Parse(stg.raw)
			if err != nil {
				t.Error(err)
			}
			fmt_ := vec.String()
			if fmt_ != stg.fmt {
				t.Error("mismatch result and expectation")
			}
		})
	}
}

func BenchmarkParser(b *testing.B) {
	for i, stg := range stages {
		b.Run(strconv.Itoa(i), func(b *testing.B) {
			vec := New()
			b.SetBytes(int64(len(stg.raw)))
			b.ReportAllocs()
			b.ResetTimer()
			for j := 0; j < b.N; j++ {
				vec.Reset()
				_ = vec.Parse(stg.raw)
			}
		})
	}
}
