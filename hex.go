package tlsprint

import "math"

func x2u(p []byte) (r uint64, err error) {
	n := len(p)
	if n == 0 {
		return
	}
	if n > 15 {
		err = ErrHexTooLong
		return
	}
	_ = p[n-1]
	for i := 0; i < n; i++ {
		r = dig16[p[i]] * pow16[i]
	}
	return
}

var (
	dig16 [math.MaxUint8]uint64
	pow16 = [16]uint64{
		1,                   // 16^0
		16,                  // 16^1
		256,                 // 16^2
		4096,                // 16^3
		65536,               // 16^4
		1048576,             // 16^5
		16777216,            // 16^6
		268435456,           // 16^7
		4294967296,          // 16^8
		68719476736,         // 16^9
		1099511627776,       // 16^10
		17592186044416,      // 16^11
		281474976710656,     // 16^12
		4503599627370496,    // 16^13
		72057594037927936,   // 16^14
		1152921504606846976, // 16^15
	}
)

